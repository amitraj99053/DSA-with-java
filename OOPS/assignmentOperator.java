public class assignmentOperator {
    public static void main(String[] args) {
        int x = 5;

        // assignment operator
        System.out.println(x);
        System.out.println(x += 3);
        System.out.println(x -= 3);
        System.out.println(x *= 3);
        System.out.println(x /= 3);
        System.out.println(x %= 3);
        System.out.println(x &= 3);
        System.out.println(x |= 3);
        System.out.println(x ^= 3);
        System.out.println(x >>= 3);
        System.out.println(x <<= 3);

        // increment operator
        System.out.println(++x);

        
    }
}
