public class Qn2 {
    // type casting
    public static void main(String[] args) {
        int a = 8;
        float b = a;
        System.out.println(b);

        float f1 = 13.99f;
        int in = (int) f1;
        System.out.println(in);

        System.out.println(f1 - a);
    }
}
